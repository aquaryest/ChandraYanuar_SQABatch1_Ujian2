package com.sqa.no7;

public class Tabungan {

	int cekSaldo, setor, tarik;

	public int getCekSaldo() {
		return cekSaldo;
	}

	public void setCekSaldo(int cekSaldo) {
		this.cekSaldo = cekSaldo;
	}

	public int getSetor() {
		return setor;
	}

	public void setSetor(int setor) {
		this.setor = setor;
	}

	public int getTarik() {
		return tarik;
	}

	public void setTarik(int tarik) {
		this.tarik = tarik;
	}
	
	

}
